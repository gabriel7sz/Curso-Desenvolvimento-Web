/*
    Início ao paradigma de Orientação a Objetos.
    Que é um dos paradigmas suportados pela linguagem de programação JavaScript.

    O que siginifica paradigma?
    Basicamente é um padrão, uma conduta, uma forma de se fazer algumca coisa.
    Ao longo da história, o desenvolvimento de software, as linguagens de programação, sofreram
    bastante modificações. Existem diversas linguagens, cada uma com suas características particulares.
    Essas características particulares acabam tornando essas linguagens especiais para algum TIPO de problema e 
    naturalmente uma dessas características é o suporte em relação aos paradigmas de desenvolvimento que podem
    ser utilizadas dentro dela.

    Por exemplo o JAVA é fortemente tipado, conhecido como uma linguagem totalmente orientada a objetos.
    Já o PHP é semelhante ao JavaScript, podendo assim escrever códigos baseados em paradigmas procedurais, que é
    popularmente conhecido como paradigma estruturado ou imperativo.

    Esses paradigmas procedurais e estruturados ou imperativos, são os mais difundidos no mercado e trabalham
    muito bem entre si. O JavaScript consegue lidar com os dois.
    Embora o paradigma orientado a objetos é considerado sucessor ao paradigma procedural, na verdade o que acontece
    na prática, é que eles se completam.

    O paradigma procedural.
    Os princípios do paradigma procedural são: O desenvolvimento de aplicações que seguem uma sequência (interpretação
    gravitacional), que possuem estruturas de decisão (condicionais) e possuem estruturas de repetição (estruturas
    interativas).
    Esses comandos podem ser utilizadas a fim de atender aos princípios que foram estabelecidos na definição do que é
    o paradigma, o padrão, ou a conduta de trabalho.

    Já em relação a orientação orientada a objetos, já temos outros pilares, sendo que esses pilares também são
    atendidos no JavaScript, possuindo os recursos que são necessários para cumprir os princípios do paradigma
    em questão.
*/ 